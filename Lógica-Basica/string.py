"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / forte
str -> string -> texto
String são textos que estão dentro de aspas
"""

# Aspas simples
print('Isso é uma string de aspas simples')

# Aspas duplas
print("Isso é uma string de aspas duplas")

# Escape
print('Isso é uma string de \'aspas simples\'')

# r -> mostra tudo que está no comando independente de tudo
print(r'Isso é uma string de \'aspas simples\'')

